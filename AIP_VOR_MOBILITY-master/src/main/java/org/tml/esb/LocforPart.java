package org.tml.esb;

public class LocforPart {
	
	String part_num;
	String user_latitude;
	String user_longitude;
	String qty;
	String kms_range;
	String app_name;
	
	public String getPart_num() {
		return part_num;
	}
	public void setPart_num(String part_num) {
		this.part_num = part_num;
	}
	public String getUser_latitude() {
		return user_latitude;
	}
	public void setUser_latitude(String user_latitude) {
		this.user_latitude = user_latitude;
	}
	public String getUser_longitude() {
		return user_longitude;
	}
	public void setUser_longitude(String user_longitude) {
		this.user_longitude = user_longitude;
	}
	public String getQty() {
		return qty;
	}
	public void setQty(String qty) {
		this.qty = qty;
	}
	public String getKms_range() {
		return kms_range;
	}
	public void setKms_range(String kms_range) {
		this.kms_range = kms_range;
	}
	public String getApp_name() {
		return app_name;
	}
	public void setApp_name(String app_name) {
		this.app_name = app_name;
	}
	
}
